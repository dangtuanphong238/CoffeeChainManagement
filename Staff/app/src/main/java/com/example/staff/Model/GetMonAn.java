package com.example.staff.Model;

public class GetMonAn {
    String gia;
    String ten;

    public GetMonAn(String gia, String ten) {
        this.gia = gia;
        this.ten = ten;
    }

    public String getGia() {
        return gia;
    }

    public void setGia(String gia) {
        this.gia = gia;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
